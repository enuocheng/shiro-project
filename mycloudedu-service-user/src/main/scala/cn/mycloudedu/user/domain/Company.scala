class Company {
  @BeanProperty var id: Integer = null
  @BeanProperty var name: String = null
  @BeanProperty var intro: String = null
  @BeanProperty var shortName: String = null
  @BeanProperty var industryId: Integer = null
  @BeanProperty var industryName: String = null
  @BeanProperty var industryTag: String = null
  @BeanProperty var property: Integer = null
  @BeanProperty var province: Integer = null
  @BeanProperty var city: Integer = null
  @BeanProperty var district: Integer = null
  @BeanProperty var address: String = null
  @BeanProperty var logo: String = null
  @BeanProperty var applyTime: Long = 0
  @BeanProperty var website: String = null
  @BeanProperty var scale: Integer = null
  @BeanProperty var tag: String = null
  @BeanProperty var approveStatus: Integer = null
  @OrderProperty
  @BeanProperty var approvedTime: Long = 0
  @BeanProperty var approveNote: String = null
  @BeanProperty var approver: String = null
  @BeanProperty var licenseUrl: String = null
  @BeanProperty var stage: Integer = null
  @BeanProperty var pictures: String = null
}
